    {
        Product::create([
            'NamaMakanan' => $request->NamaMakanan,
            'AsalMakanan' => $request->AsalMakanan,
            'TanggalExpired' => $request->TanggalExpired,
            'Kuantitas' => $request->Kuantitas
        ]);
        return redirect('/home');
    }

